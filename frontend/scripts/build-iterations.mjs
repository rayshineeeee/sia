// Loads real convergence renders into the carousel's iteration data.
// Reads ../runs/run_<N>/gen_*/{render.png,results.json,prompt.txt,improvement.md},
// copies each render.png into public/iterations/, and writes
// src/components/iterations/iterations.generated.ts (generatedRenders, index = iteration order).
// converge.py calls this after EACH iteration -> Next dev HMR -> live carousel.
// Usage: node scripts/build-iterations.mjs [runNumber]   (default 9)
import { fileURLToPath } from "node:url";
import path from "node:path";
import fs from "node:fs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const FRONTEND = path.resolve(__dirname, "..");
const REPO_ROOT = path.resolve(FRONTEND, "..");
const RUNS = path.join(REPO_ROOT, "runs");
const PUB = path.join(FRONTEND, "public", "iterations");
const OUT = path.join(FRONTEND, "src", "components", "iterations", "iterations.generated.ts");

const runN = Number(process.argv[2] || 9);
const runDir = path.join(RUNS, `run_${runN}`);
if (!fs.existsSync(runDir)) {
  console.log(`[build-iterations] no run dir at ${runDir} — leaving stub.`);
  process.exit(0);
}

const readJson = (p) => { try { return JSON.parse(fs.readFileSync(p, "utf8")); } catch { return {}; } };
const firstLine = (p) => { try { return fs.readFileSync(p, "utf8").split("\n").map((s) => s.replace(/^#+\s*/, "").trim()).find(Boolean); } catch { return undefined; } };
const snip = (p, m = 140) => { try { const t = fs.readFileSync(p, "utf8").trim(); return t.length > m ? `${t.slice(0, m)}…` : t; } catch { return undefined; } };

const gens = fs.readdirSync(runDir)
  .filter((n) => /^gen_\d+$/.test(n))
  .map((n) => ({ num: Number(n.split("_")[1]), dir: path.join(runDir, n) }))
  .filter((g) => fs.existsSync(path.join(g.dir, "render.png")) && fs.existsSync(path.join(g.dir, "results.json")))
  .sort((a, b) => a.num - b.num);

fs.mkdirSync(PUB, { recursive: true });

let best = -Infinity;
const items = gens.map((g, i) => {
  const r = readJson(path.join(g.dir, "results.json"));
  const acc = typeof r.accuracy === "number" ? r.accuracy : 0;
  best = Math.max(best, acc);
  const img = `iter_${String(i + 1).padStart(3, "0")}.png`;
  fs.copyFileSync(path.join(g.dir, "render.png"), path.join(PUB, img));
  const improvement = firstLine(path.join(g.dir, "improvement.md"));
  const prompt = snip(path.join(g.dir, "prompt.txt"));
  return {
    renderUrl: `/iterations/${img}`,
    accuracy: Number(acc.toFixed(2)),
    originalPrompt: prompt,
    refinedPrompt: improvement,
    summary: `taste ${acc.toFixed(1)}${improvement ? ` · ${improvement}` : ""}`,
  };
});

const banner = `// AUTO-GENERATED by scripts/build-iterations.mjs — do not edit by hand.\nimport type { Iteration } from "./types";\n\n`;
const body =
  `export const generatedRenders: Partial<Iteration>[] = ${JSON.stringify(items, null, 2)};\n\n` +
  `export const generatedMeta = ${JSON.stringify({ runId: runN, count: items.length, bestAccuracy: Number((best === -Infinity ? 0 : best).toFixed(2)) }, null, 2)} as const;\n`;
fs.writeFileSync(OUT, banner + body);
console.log(`[build-iterations] run_${runN}: ${items.length} renders -> public/iterations + iterations.generated.ts (best ${best === -Infinity ? 0 : best.toFixed(1)})`);
